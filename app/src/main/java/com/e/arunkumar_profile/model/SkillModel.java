package com.e.arunkumar_profile.model;

public class SkillModel {

    int image;
    String name;

    public SkillModel(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
